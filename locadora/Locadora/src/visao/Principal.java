    private void jSalvarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSalvarCarroActionPerformed
        Carro carro = new Carro();
        carro.setPlaca(jPlaca.getText());
        carro.setAno(jAno.getText());
        carro.setVl_diaria(Double.parseDouble(jValor.getText()));
       Modelo modelo = (Modelo) jComboModelo.getSelectedItem();
       carro.setModelo(modelo);
       
        try {
            carroDao.cadastrar(carro);
            loadCarro();
            JOptionPane.showMessageDialog(null,"Cadastro Realizado!");
            
           
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jSalvarCarroActionPerformed

