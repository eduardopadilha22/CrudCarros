    public void loadModelo(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[]{"código","desc","fabricante"}, 0);
        for (int i = 0; i < listModelo.size(); i++) {
            modelo.addRow(new Object[]{listModelo.get(i).getId(),listModelo.get(i).getDescricao(),listModelo.get(i).getFabricante().getNome()});
        }
        jTblModelo.setModel(modelo);
        
    }
    
    public void loadCarro(){
        DefaultTableModel modelo2 = new DefaultTableModel(new Object[]{"placa","ano","valor Diária","modelo"}, 0);
        for (int i = 0; i < listCarro.size(); i++) {
             modelo2.addRow(new Object[]{listCarro.get(i).getPlaca(),listCarro.get(i).getAno(),listCarro.get(i).getVl_diaria(),listCarro.get(i).getModelo().getDescricao()});
        }
        jTblCarro.setModel(modelo2);
    }
